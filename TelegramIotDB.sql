USE TelegramIotDB

DROP TABLE IF EXISTS SCHEDULES;
DROP TABLE IF EXISTS PERIPHERALS;
DROP TABLE IF EXISTS DEVICES;
DROP TABLE IF EXISTS ROOMS;
DROP TABLE IF EXISTS BOTS;
DROP TABLE IF EXISTS HOMES;
DROP TABLE IF EXISTS ACTIONS;
DROP TABLE IF EXISTS ACTIONTYPE;

CREATE TABLE HOMES
(
    ID                      INT             IDENTITY(1,1) PRIMARY KEY,
    CEP     				INT		        NOT NULL,
    NUMBER                  INT             NOT NULL,
    NAME     				VARCHAR(50)		NOT NULL,
    ADDRESS     			VARCHAR(80)		NOT NULL,
	DATE					DATETIME 		NOT NULL
);
ALTER TABLE HOMES ADD CONSTRAINT UK_HOMES UNIQUE(CEP, NUMBER);

CREATE TABLE BOTS
(
    ID                      INT             IDENTITY(1,1) PRIMARY KEY,
    HOMEID                  INT             FOREIGN KEY REFERENCES HOMES(ID),
    INDENTYTOKEN 			VARCHAR(250)	NOT NULL,
    AUTHTOKEN 				VARCHAR(250)	NOT NULL,
    CHATID                  BIGINT			NOT NULL,
    OWNER   				VARCHAR(40)		NOT NULL,
    BOTALIAS                VARCHAR(40)		NOT NULL,
    BOTNAME 				VARCHAR(40)		NOT NULL,
	DATE					DATETIME 		NOT NULL 
);
ALTER TABLE BOTS ADD CONSTRAINT UK_BOTS UNIQUE(INDENTYTOKEN, AUTHTOKEN, CHATID);

CREATE TABLE ROOMS
(
    ID                      INT             IDENTITY(1,1) PRIMARY KEY,
    HOMEID                  INT             FOREIGN KEY REFERENCES HOMES(ID),
    NAME     				VARCHAR(40)		NOT NULL,
	DATE					DATETIME 		NOT NULL
);
ALTER TABLE ROOMS ADD CONSTRAINT UK_ROOMS UNIQUE(NAME);

CREATE TABLE DEVICES
(
    ID                      INT             IDENTITY(1,1) PRIMARY KEY,
    HOMEID                  INT             FOREIGN KEY REFERENCES HOMES(ID),
    ROOMID                  INT             FOREIGN KEY REFERENCES ROOMS(ID),
    UID     				VARCHAR(40)		NOT NULL,
    DATE					DATETIME 		NOT NULL
);
ALTER TABLE DEVICES ADD CONSTRAINT UK_DEVICES UNIQUE(UID);

CREATE TABLE PERIPHERALS
(
    ID                      INT             IDENTITY(1,1) PRIMARY KEY,
    DEVICEID                INT             FOREIGN KEY REFERENCES DEVICES(ID),
    NAME     				VARCHAR(40)		NOT NULL,
    GPIO                    INT             NOT NULL,
    TYPE                    VARCHAR(2)		NOT NULL,
    ISANALOGIC              BIT             NOT NULL,
    DATE					DATETIME 		NOT NULL
);
ALTER TABLE PERIPHERALS ADD CONSTRAINT UK_PERIPHERALS UNIQUE(DEVICEID, NAME, GPIO);


CREATE TABLE SCHEDULES
(
    ID                      INT             IDENTITY(1,1) PRIMARY KEY,
    DEVICEID                INT             FOREIGN KEY REFERENCES DEVICES(ID),
    PERIPHERALID            INT             FOREIGN KEY REFERENCES PERIPHERALS(ID),
    FREQUENCY               VARCHAR(1)      NOT NULL, --D - DIARIA, S - SEMANAL, Q - M - MENSAL
    DAYOFWEEK1              VARCHAR(3)      NOT NULL, --DOM, SEG, TER, QUA, QUI. SEX. SAB
    DAYOFMONTH1             INT             NOT NULL, --DE 1 A 28
    STARTHOUR               INT             NOT NULL, --DE 0 A 23
    STARTMINUTES            INT             NOT NULL, --DE 0 A 59 
    TIMEMINUTES             INT             NOT NULL, --DE 1 A ... 
    TIMES                   INT             NOT NULL, --DE 1 A ...
    REPEATMINUTESFREQUECY   INT             NOT NULL  --DE 0 A ...
                                                      --S TER 11 30 15 2 60
                                                      -- A ESTRATEGIA E DAR UPDATE NA TABELA A CADA MENSAGEM ATÉ COMPLETAR TODOS OS CAMPÓS
);
ALTER TABLE SCHEDULES ADD CONSTRAINT UK_SCHEDULES UNIQUE(DEVICEID, PERIPHERALID);

CREATE TABLE ACTIONTYPE
(
    ID                      INT             PRIMARY KEY,
    ACTIONTYPENAME     		VARCHAR(40)		NOT NULL
);
ALTER TABLE ACTIONTYPE ADD CONSTRAINT UK_ACTIONTYPE UNIQUE(ACTIONTYPENAME);

CREATE TABLE ACTIONS
(
    ID                      INT             IDENTITY(1,1) PRIMARY KEY,
    ACTIONTYPEID            INT             FOREIGN KEY REFERENCES ACTIONTYPE(ID),
    ACTIONNAME     			VARCHAR(40)		NOT NULL,
    ACTIONCOMMAND           VARCHAR(40)		NOT NULL
);
ALTER TABLE ACTIONS ADD CONSTRAINT UK_ACTIONS UNIQUE(ACTIONNAME);

